# 作业：
# 使用*打印三角形
# 整数代表的是距离最左边的空格数。
# 3
#    *     2*(n-3)+1
#   ***    2*(n-2)+1
#  *****   2*(n-1)+1
# *******  2*n+1

print("   *")
print("  ***")
print(" *****")
print("*******")

n =3
print(n*" "+"*"*(2*(n-3)+1))
print((n-1)*" "+"*"*(2*(n-2)+1))
print((n-2)*" "+"*"*(2*(n-1)+1))
print((n-3)*" "+"*"*(2*n+1))
